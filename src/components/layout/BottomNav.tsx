'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { clsx } from 'clsx'
import { useAuth } from '@/contexts/AuthContext'

const NAV_ITEMS = [
  { href: '/dashboard', label: 'Home', icon: GridIcon },
  { href: '/jogos', label: 'Jogos', icon: TrophyIcon },
  { href: '/classificacao', label: 'Tabela', icon: TableIcon },
  { href: '/meus-palpites', label: 'Palpites', icon: PencilIcon },
  { href: '/ranking', label: 'Ranking', icon: ChartIcon },
  { href: '/perfil', label: 'Perfil', icon: UserIcon },
]

export function BottomNav() {
  const pathname = usePathname()
  const { user } = useAuth()

  const items = [
    ...NAV_ITEMS,
    ...(user?.isAdmin ? [{ href: '/admin/resultados', label: 'Admin', icon: SettingsIcon }] : []),
  ]

  return (
    <nav className="md:hidden fixed bottom-0 left-0 right-0 z-30 bg-surface border-t border-light-gray">
      <div className="flex">
        {items.map(({ href, label, icon: Icon }) => {
          const active = pathname.startsWith(href)
          return (
            <Link
              key={href}
              href={href}
              className={clsx(
                'flex-1 flex flex-col items-center gap-0.5 py-2 text-xs font-medium transition-colors',
                active ? 'text-dark' : 'text-mid-gray',
              )}
            >
              <Icon className={clsx('w-6 h-6', active ? 'text-primary' : 'text-mid-gray')} />
              <span>{label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}

